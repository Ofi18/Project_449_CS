package com.ofi.project_449_cs;



 class InstantMassage {
     private String message;
     private String author;

     InstantMassage(String message, String author) {
         this.message = message;
         this.author = author;
     }

     public InstantMassage() {



     }

     String getMessage() {
         return message;
     }

     String getAuthor() {
         return author;
     }
 }

